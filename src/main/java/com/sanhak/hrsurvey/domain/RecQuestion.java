package com.sanhak.hrsurvey.domain;

public class RecQuestion {
	private int recQNum;
	private String recQKr;
	private String recQEn;
	public int getRecQNum() {
		return recQNum;
	}
	public void setRecQNum(int recQNum) {
		this.recQNum = recQNum;
	}
	public String getRecQKr() {
		return recQKr;
	}
	public void setRecQKr(String recQKr) {
		this.recQKr = recQKr;
	}
	public String getRecQEn() {
		return recQEn;
	}
	public void setRecQEn(String recQEn) {
		this.recQEn = recQEn;
	}
}
